package t_arrayInJava;

public class Example4 {
		// Method with 1D return type
			public int[] test1() {
			return  new int [] {1,2,3,4 };
 		}
	
		
		// Method with 2D return type
		public int[][] test2() {
			return new int[][] {{1,2}, {3,4}, {5,6}, {7,8}};
		}
		
		// Method with 2D array with return type as object
		public Object[][] test3() {
			return new Object[][] {{1,2}, {3,4}, {5,6}, {7,8}};
		}
		
		
 }
