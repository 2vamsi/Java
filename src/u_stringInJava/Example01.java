package u_stringInJava;

/*
 * 
 * Example of showing that  - a String is a Sequence of characters
 *
 */


public class Example01 {

	public static void main(String[] args) {
		
		char[] ch = {'V','a','m','s','i','.','D',};
		String s = new String(ch);
		System.out.println(s);
		// when we print it, character array gets converted into String ( this is one way of doing it)
		
		
	}
		
}
