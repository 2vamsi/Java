package a_operators8910;

public class Example1 {

	
	public static void main(String[] args) {
		
		int i=5;
		
		System.out.println(i);
		// example of post-incremental and pre-incremental operators
		System.out.println(i++);
		System.out.println(++i);
		
		// example of post-decremental and pre-decremental operators
		System.out.println(i--);
		System.out.println(--i);
		
		System.out.println("*************");

	}
	

}
